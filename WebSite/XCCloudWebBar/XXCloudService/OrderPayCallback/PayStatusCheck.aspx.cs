using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;
using XCCloudWebBar.OrderPayCallback.Common;

namespace XXCloudService.PayChannel
{
    public partial class PayStatusCheck : System.Web.UI.Page
    {
        protected void Page_Load(object sender, EventArgs e)
        {
            string tradeNum = Request["tradenum"];
            string money = "";
            if (PayList.GetItem(tradeNum, out money))
                Response.Write("success:" + tradeNum + ":" + money);
            else
                Response.Write("fail");
        }
    }
}